package section07;

public class Class57 {
    public static void main(String[] args) {

        // operadores bitwise - usando em programação em baixo nível
        int x = 84;
        int y = 60;

        System.out.println(x & y); // AND
        System.out.println(x | y); // OR
        System.out.println(x ^ y); // XOR
    }
}
